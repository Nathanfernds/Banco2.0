public class Cliente {
    
    String nome;
    String conta;
    String agenciaString;
    float saldo;
    
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getConta() {
        return conta;
    }

    public void setConta(String conta) {
        this.conta = conta;
    }

    public String getAgenciaString() {
        return agenciaString;
    }

    public void setAgenciaString(String agenciaString) {
        this.agenciaString = agenciaString;
    }

    public float getSaldo() {
        return saldo;
    }

    public void setSaldo(float saldo) {
        this.saldo = saldo;
    }
    
    
    
    
}
